  <div class="body">
    <div class="content">
      <h3>Anomaly class</h3>
      <p>ENT/Bippa</p>
      <h3>Anomaly description</h3>
      <p>
        ENT#344 is a post on the Internet blog ███.████.com (referred to as ENT#344-A) titled
        "Solving the Greatest Crisis of Our Time".
      </p>
      <p>
        ENT#344-A is a collection of HTML, CSS, and JavaScript files currently hosted on a Debian
        Linux virtual machine (VM) in a German datacenter. No changes to the base Debian image have
        been found except the installation of Nginx, and the addition of Nginx configuration files
        to serve ENT#344-A from its' domain name ███.████.com.
      </p>
      <p>
        Attempts to wipe ENT#344-A off the VM have resulted in it being replaced a few hours later
        with any reporting logs missing. Attemps to wipe the VM off its server or destroying the
        server have resulted in another VM to be created on a different server with ENT#344-A being
        served. Attempts to modify ENT#344-A to remove ENT#344, or blacklist ███.████.com have also
        been met with similar failure.
      </p>
      <p>No other blog post on ENT#344-A appears to have anomalous properties.</p>
      <p>
        The contents of ENT#344 describe the "greatest crisis of our time" to be the French
        (stylised as "<img
          alt="france flag"
          style="width: 20px;"
          src="/resources/spe_001/france_flag.png"
        /> The French") "near-total" domination of the World Table Tennis Championships (WTTC) organised
        by the International Table Tennis Federation. ENT#344 will propose methods to solve the "crisis"
        with varying levels of viability. The most absurd of which is to "use a larger and heavier ping-pong
        ball (which requires bigger rackets) and scale up the table so that it's big enough to stand
        on."
      </p>
      <p>
        At the end of the post, ENT#344 will attempt a call to action and have the reader submit
        their own ideas for solving the "crisis". However, submitting something that would stop the
        French near-total domination of the WTTC will do nothing but display a message of
        congratulations. Instead, submitting something to make the French "near-total" domination
        into "total" domination will cause a Reality Restructuring event that implements the idea.
        ENT#344 will then be replaced with a normal blog post and all anomalous properties of
        ENT#344-A will disappear.
      </p>
      <p>
        Experiments have shown that ENT#344 is not dependant on French domination of the WTTC, and
        if introduced to a para-reality with French domination of any sport, it will adapt its
        content and resulting Reality Restructuring event to accomodate.
      </p>
      <p>
        ENT#344 was identified and contained in our reality due to discrepancies in world history
        (mostly around the English Civil War of the 1600s) after the Reality Rollback event of 20█1.
      </p>
      <h3>Research procedures</h3>
      <p>
        Research and experimentation on ENT#344 in our reality is forbidden and is punishable with
        the immediate revocation of credentials and blacklisting from AARO and the Australian
        Science Society (ASS). All researchers must use █████ to create new para-realities by
        forking and modifying the "French sport domination" preset created by Dr. ███, which has
        been specfically made for ENT#344.
      </p>
      <p>
        ENT#344 is to then be introduced into the para-reality using <a
          href="https://rsync.samba.org"
          target="_blank">rsync</a
        > to copy ENT#344-A into the ██████ Proxy, which can then be accessed in the para-reality using
        the Kilian Internet Browser.
      </p>
      <p>
        A sport can only be tested on ENT#344 once and must be approved by a member of the AARO
        board. Since there can be many sporting championships for a single sport, all experiments of
        ENT#344 on a specific sport must be grouped together within a timeframe of 3 months before
        that sport is unable to be tested on again.
      </p>
      <p>
        Any attempts to reverse engineer ENT#344-A or ENT#344 must also be done in a para-reality.
      </p>
      <h3>Previous experiments</h3>
      <p>
        ENT#344 has been experimented on in multiple para-realities with sporting competitions
        organised by the following organisations:
      </p>
      <ul>
        <li>Badminton World Federation (BWF)</li>
        <li>Fédération Internationale des Échecs (FIDE)</li>
        <li>International Golf Federation (IGF)</li>
        <li>International Weightlifting Federation (IWF)</li>
        <li>Fédération Internationale de Football Association (FIFA)</li>
        <li>International Cricket Council (ICC)</li>
        <li>Fédération Internationale de l'Automobile (FIA)</li>
      </ul>
      <h3>Miscellaneous info</h3>
      <ul>
        <li>
          ENT#344 is the third anomaly AARO has found in an Internet website, the previous two being
          ENT#██ and ENT#███. This has raised some concerns on the openness of the Internet to the
          general public.
        </li>
        <li>
          ENT#344 was upgraded from an anomaly class of Wooluu to Bippa after an experiment with <a
            href="https://en.wikipedia.org/wiki/Code_golf"
            target="_blank">code golf</a
          > caused a fourth World War involving every Francophone country.
        </li>
        <li>
          AARO was approached by the Australian Agency for Rockets and Space Exploration (AARSE) to
          share information on ENT#344, citing "security concerns". The request was denied by board
          member ███, saying "why the hell is AARSE trying to lecture us on security? Go back to
          trying to grow potatoes on Mars you f██ng c█ts." AARSE has not communicated with AARO
          since.
        </li>
        <li>
          Ethical concerns have been raised as a result of the research procedures creating entirely
          new realities to just, as a leaked chat message from board member ████ put it: "[let's]
          see how messed up it can get! It'll be funny! C'mon guys!" These concerns have been
          dismissed and board member ████s' statement has been retracted.
        </li>
      </ul>
